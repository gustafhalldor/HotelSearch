    public String buildSqlstring(boolean sightseeing, boolean fishing, boolean hiking, boolean glacier, String location, String fDate, String tDate) {
        String sqlstatement = new String();
        sqlstatement = "SELECT * FROM Trips WHERE (fromDate BETWEEN '"+ fDate + "' AND '" + tDate + "') AND location = '" + location + "'";
        int count = 0;
        
        if(sightseeing == true) {
            count++;
        }
        if(fishing == true) {
            count++;
        }
        if(hiking == true) {
            count++;
        }
        if(glacier == true) {
            count++;
        }
        

        //Special case location = "Any"
        if(location.equals("Any")) {
            sqlstatement = "SELECT * FROM Trips WHERE (fromDate BETWEEN '"+ fDate + "' AND '" + tDate + "')";
        }
        
        if(count == 1) {
            if(sightseeing == true) {
                sqlstatement = sqlstatement + "AND type = 'Sightseeing'";
            }
            if(fishing == true) {
                sqlstatement = sqlstatement + "AND type = 'Fishing'";
            }
            if(hiking == true) {
                sqlstatement = sqlstatement + "AND type = 'Hiking'";
            }
            if(glacier == true) {
                sqlstatement = sqlstatement + "AND type = 'Glacier'";
            }
        }
        
        if(count == 2) {
            if(sightseeing == true && fishing == true) {
                sqlstatement = sqlstatement + "AND (type = 'Sightseeing' OR type = 'Fishing')";
            }
            if(sightseeing == true && hiking == true) {
                sqlstatement = sqlstatement + "AND (type = 'Sightseeing' OR type = 'Hiking')";
            }
            if(sightseeing == true && glacier == true) {
                sqlstatement = sqlstatement + "AND (type = 'Sightseeing' OR type = 'Glacier')";
            }
            if(fishing == true && hiking == true) {
                sqlstatement = sqlstatement + "AND (type = 'Fishing' OR type = 'Hiking')";
            }
            if(fishing == true && glacier == true) {
                sqlstatement = sqlstatement + "AND (type = 'Fishing' OR type = 'Glacier')";
            }
            if(hiking == true && glacier == true) {
                sqlstatement = sqlstatement + "AND (type = 'Hiking' OR type = 'Glacier')";
            }
        }
        
        if(count == 3) {
            if(sightseeing == true && fishing == true && hiking == true) {
                sqlstatement = sqlstatement + "AND (type = 'Sightseeing' OR type = 'Fishing' OR type = 'Hiking')";
            }
            if(sightseeing == true && fishing == true && glacier == true) {
                sqlstatement = sqlstatement + "AND (type = 'Sightseeing' OR type = 'Fishing' OR type = 'Glacier')";
            }
            if(sightseeing == true && hiking == true && glacier == true) {
                sqlstatement = sqlstatement + "AND (type = 'Sightseeing' OR type = 'Hiking' OR type = 'Glacier')";
            }
            
            if(fishing == true && hiking == true && glacier == true) {
                sqlstatement = sqlstatement + "AND (type = 'Fishing' OR type = 'Hiking' OR type = 'Glacier')";
            }
        }
        
        if(count == 4) {
            if(sightseeing == true && fishing == true && hiking == true && glacier == true) {
                sqlstatement = sqlstatement + "AND (type = 'Sightseeing' OR type = 'Fishing' OR type = 'Hiking' OR type = 'Glacier')";
            }
        }
        
        System.out.println(count);

        return sqlstatement+";";
    }
    
    
